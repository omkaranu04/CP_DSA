#include <bits/stdc++.h>
using namespace std;
#define ll long long int
void solve()
{
    string a, b, c;
    cin >> a >> b >> c;
    cout << a[0] << b[0] << c[0] << "\n";
    return;
}
int main(int argc, char const *argv[])
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    ll t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}